// Prob2: Data Conversion (Explicit Type Casting)
public class Prob2 {
    public static void main(String[] args) {
        double originalValue = 9.78;
        int convertedValue = (int) originalValue; // Explicit type casting

        System.out.println("Original value (double): " + originalValue);
        System.out.println("Converted value (int): " + convertedValue);
    }
}
